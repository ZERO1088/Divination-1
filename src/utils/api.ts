/** 生成塔罗 Demo 回复 */
export function generateTarotDemoReply(params: { cardName: string; isReversed: boolean; question: string }): string {
  const { cardName, isReversed, question } = params;
  
  // 通用牌义（未找到专属模板时使用）
  const genericUpright = '这张牌正位提示你关注当前事态的核心能量。保持觉察，顺势而为。';
  const genericReversed = '这张牌逆位提示你注意能量的内化或受阻。重新审视自己的态度和方向。';
  
  const template = TAROT_DEMO_TEMPLATES[cardName];
  if (!template) {
    const base = isReversed ? genericReversed : genericUpright;
    return `【牌意解读】\n${base}\n\n【对应问题分析】\n（Demo 模式：配置 API Key 获取详细解读）\n\n【建议】\n保持开放心态，相信自己的直觉。`;
  }
  
  const interpretation = isReversed ? template.reversed : template.upright;
  
  return (
    '【牌意解读】\n' +
    interpretation.popular +
    '\n\n【对应问题分析】\n' +
    `针对您的问题"${question || '未说明'}"，` +
    (isReversed
      ? '逆位显示该能量目前处于受阻或内化状态，可能需要更多时间来整合。'
      : '正位显示该能量正在正常运作，可以顺势推进。') +
    '\n\n【建议】\n' +
    (isReversed
      ? '给自己更多时间和空间，不要急于求成。重新审视内在的态度和动机。'
      : '保持当前的状态和方向，相信自己的选择，适时行动。')
  );
}
