/** 清理 AI 返回内容中的 Markdown 标记，适配纯文本网页展示 */
export function stripMarkdown(text: string): string {
  return text
    // 标题标记：## → 空（保留内容），匹配行首或段落开头的 #
    .replace(/^#{1,6}\s+/gm, '')
    .replace(/\n#{1,6}\s+/g, '\n')
    // 粗体/斜体：**text** → text，*text* → text
    .replace(/\*\*(.+?)\*\*/g, '$1')
    .replace(/\*(.+?)\*/g, '$1')
    // 行内代码：`text` → text
    .replace(/`(.+?)`/g, '$1')
    // 无序列表：- item → · item（保留结构，去掉 Markdown 味）
    .replace(/^- /gm, '· ')
    // 连续空行压缩为单个空行
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

// ---- 错误分类 ----

/** 错误分类工具 */
export function classifyError(err: unknown): APIError {
  if (err instanceof Error) {
    if (err.name === 'AbortError') {
      return { message: '请求超时，请稍后重试', type: 'timeout' };
    }
    if (err.message.includes('fetch')) {
      return { message: '网络连接失败，请检查网络', type: 'network' };
    }
    if (err.message.includes('500') || err.message.includes('502') || err.message.includes('503')) {
      return { message: '服务器繁忙，请稍后重试', type: 'server' };
    }
    return { message: err.message, type: 'unknown' };
  }
  return { message: '发生未知错误', type: 'unknown' };
}

