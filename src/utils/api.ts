/** 清理 AI 返回内容中的 Markdown 符号，保留格式结构 */
export function cleanMarkdown(text: string): string {
  return text
    // 移除标题符号（# ## ### 等）
    .replace(/^#{1,6}\s+/gm, '')
    // 移除粗体标记 **text** -> text
    .replace(/\*\*(.+?)\*\*/g, '$1')
    // 移除斜体标记 *text* 或 _text_ -> text
    .replace(/(?<!\*)\*([^*]+)\*(?!\*)/g, '$1')
    .replace(/_([^_]+)_/g, '$1')
    // 移除行内代码标记 `code` -> code
    .replace(/`([^`]+)`/g, '$1')
    // 移除分隔线 --- 或 *** 或 ---
    .replace(/^[\*\-_]{3,}\s*$/gm, '')
    // 移除无序列表标记 - 或 * 或 + 
    .replace(/^[\-\*\+]\s+/gm, '')
    // 移除有序列表标记 1. 2. 等
    .replace(/^\d+\.\s+/gm, '')
    // 清理多余的空行（超过2个空行改为1个）
    .replace(/\n{3,}/g, '\n\n')
    // 清理行首行尾空格
    .trim();
}

/** 检测场景类型（用于辅助 AI 理解） */
function detectScene(question: string): string {
  const q = question.toLowerCase();
  if (/求职|工作|面试|跳槽|offer|职业|事业|岗位|入职|简历|薪资/.test(q)) return '职业求职';
  if (/感情|恋爱|爱情|分手|复合|暗恋|表白|对象|脱单|相亲|婚姻|前任/.test(q)) return '感情关系';
  if (/考试|学习|学业|考研|高考|成绩|复习|论文|毕业|留学|雅思|托福/.test(q)) return '学业考试';
  return '生活抉择';
}

